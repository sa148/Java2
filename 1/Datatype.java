class Datatype {
  public static void main(String[] args) {
    int month = 12;
    int day = 10;
    float weight = 63.00f;
    double height = 168.0;
    char bloodtype = 'A';

    String name = "菅原";

    System.out.println("こんにちは！" + name + "です");
    System.out.println("身長は" + height + "cm, 体重は" + weight + "kg, ");
    System.out.println("誕生日は" + month + day + "日、");
    System.out.println("血液型は" + bloodtype + "型です。");
    System.out.println("チャンネル登録お願いします");
  }
}